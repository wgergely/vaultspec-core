@dataclass
class ToolConfig:
    """Configuration for a single AI tool destination (e.g. claude, gemini).

    Attributes:
        name: Short identifier for the tool (e.g. ``"claude"``).
        rules_dir: Directory where synced rule files are written, or ``None``
            if the tool does not use rules files.
        skills_dir: Directory where synced skill files are written, or ``None``
            if the tool does not use skill files.
        agents_dir: Directory where synced agent files are written, or ``None``
            if the tool does not use agent files.
        config_file: Path to the tool's main config file (e.g. ``CLAUDE.md``),
            or ``None`` if not applicable.
        native_config_file: Path to a tool-native configuration file that is
            not markdown-based (for example ``.codex/config.toml``), or
            ``None`` if not applicable.
        rule_ref_dir: Directory whose markdown files should be referenced from
            ``config_file``. Defaults to ``rules_dir`` when omitted.
        rule_ref_config_file: Optional secondary config file that carries
            ``@rules/...`` references, separate from ``config_file``.
            Currently unused; retained for future providers that need a
            secondary config file distinct from their root config.
        system_file: Path to the tool's system prompt file, or ``None`` if the
            tool does not have a dedicated system file.
        emit_system_rule: Whether shared system content should be materialized
            as a rule file when ``system_file`` is absent.
        capabilities: Declared provider capabilities.  Used by install, sync,
            and dry-run to reason about what each provider supports.
    """

    name: str
    rules_dir: Path | None
    skills_dir: Path | None
    agents_dir: Path | None = None
    config_file: Path | None = None
    native_config_file: Path | None = None
    rule_ref_dir: Path | None = None
    rule_ref_config_file: Path | None = None
    system_file: Path | None = None
    emit_system_rule: bool = True
    capabilities: frozenset[ProviderCapability] = frozenset()


@dataclass
class SyncResult:
    """Accumulator for file synchronization statistics.

    Attributes:
        added: Number of files newly created at the destination.
        updated: Number of existing files that were overwritten.
        skipped: Number of files that were intentionally not written
            (e.g. transform returned ``None``, or safety guard triggered).
        pruned: Number of stale destination files that were deleted.
        errors: List of error message strings for files that failed to sync.
        items: Per-file action log — list of ``(path, action)`` tuples for
            dry-run rendering.
        warnings: Advisories about what ``--force`` would change beyond the
            default sync.
    """

    added: int = 0
    updated: int = 0
    skipped: int = 0
    pruned: int = 0
    errors: list[str] = field(default_factory=list)
    items: list[tuple[str, str]] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


