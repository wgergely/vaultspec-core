def _collect_rule_refs(cfg: ToolConfig) -> list[str]:
    """Collect rule file references."""
    if cfg.rules_dir is None or cfg.config_file is None:
        if cfg.name == "agents" and cfg.config_file:
            config_dir = cfg.config_file.parent
            refs = []
            sources = collect_rules()
            for name in sorted(sources.keys()):
                src_path, _meta, _body = sources[name]
                try:
                    rel = src_path.relative_to(config_dir)
                    refs.append(str(rel).replace("\\", "/"))
                except ValueError:
                    ref = src_path.relative_to(ROOT_DIR)
                    refs.append(str(ref).replace("\\", "/"))
            return refs
        return []
    if not cfg.rules_dir.exists():
        return []

    config_dir = cfg.config_file.parent
    refs = []
    for rule_file in sorted(cfg.rules_dir.glob("*.md")):
        try:
            rel = rule_file.relative_to(config_dir)
            refs.append(str(rel).replace("\\", "/"))
        except ValueError:
            ref = rule_file.relative_to(ROOT_DIR)
            refs.append(str(ref).replace("\\", "/"))
    return refs


def _generate_config(cfg: ToolConfig) -> str | None:
    """Generate config file content."""
    if not FRAMEWORK_CONFIG_SRC.exists():
        return None

    internal_body = FRAMEWORK_CONFIG_SRC.read_text(encoding="utf-8").strip()
    custom_body = (
        PROJECT_CONFIG_SRC.read_text(encoding="utf-8").strip()
        if PROJECT_CONFIG_SRC.exists()
        else ""
    )

    fm = {"system_framework": internal_body}

    # Body starts with custom content
    body_parts = []
    if custom_body:
        body_parts.append(custom_body)

    # Add rules
    refs = _collect_rule_refs(cfg)
    if refs:
        if body_parts:
            body_parts.append("")
        body_parts.append("## Rules")
        body_parts.append("")
        body_parts.append("You MUST respect these rules at all times:")
        body_parts.append("")
        for ref in refs:
            body_parts.append(f"@{ref}")

    body = "\n".join(body_parts)
    return f"{CONFIG_HEADER}\n{build_file(fm, body)}"


def _is_cli_managed(path: Path) -> bool:
    """Check if a config file was generated by this CLI."""
    if not path.exists():
        return False
    try:
        content = path.read_text(encoding="utf-8")
        return content.startswith(CONFIG_HEADER)
    except Exception:
        return False


def config_show(_args: argparse.Namespace) -> None:
    """Display internal/custom content and references."""
    if not FRAMEWORK_CONFIG_SRC.exists():
        rel = FRAMEWORK_CONFIG_SRC.relative_to(ROOT_DIR)
        print(f"No framework config found at {rel}")
        return

    print(f"Framework: {FRAMEWORK_CONFIG_SRC.relative_to(ROOT_DIR)}")
    print("-" * 60)
    print(FRAMEWORK_CONFIG_SRC.read_text(encoding="utf-8").strip())
    print("-" * 60)

    if PROJECT_CONFIG_SRC.exists():
        print(f"Project: {PROJECT_CONFIG_SRC.relative_to(ROOT_DIR)}")
        print("-" * 60)
        print(PROJECT_CONFIG_SRC.read_text(encoding="utf-8").strip())
        print("-" * 60)

    print("Generated references per tool:")
    for tool_name, cfg in TOOL_CONFIGS.items():
        if cfg.config_file is None:
            continue
        refs = _collect_rule_refs(cfg)
        dest_rel = cfg.config_file.relative_to(ROOT_DIR)
        managed = "CLI-managed" if _is_cli_managed(cfg.config_file) else "custom"
        print(f"\n  {tool_name} ({dest_rel}) [{managed}]:")
        if refs:
            for ref in refs:
                print(f"    @{ref}")
        else:
            print("    (no rules synced yet)")


def config_sync(args: argparse.Namespace) -> None:
    """Sync internal and custom configs to tool configs."""
    dry_run = getattr(args, "dry_run", False)
    force = getattr(args, "force", False)

    if not FRAMEWORK_CONFIG_SRC.exists():
        print("  Error: No framework config found at .vaultspec/FRAMEWORK.md")
        return

    result = SyncResult()

    for _tool_name, cfg in TOOL_CONFIGS.items():
        if cfg.config_file is None:
            continue

        content = _generate_config(cfg)
        if content is None:
            result.skipped += 1
            continue

        rel = cfg.config_file.relative_to(ROOT_DIR)

        # Safety guard
        if (
            cfg.config_file.exists()
            and not _is_cli_managed(cfg.config_file)
            and not force
        ):
            print(f"    [SKIP] {rel} - file exists with custom content.")
            msg = "           Migrate to .vaultspec/PROJECT.md, then --force."
            print(msg)
            result.skipped += 1
            continue

        action = "[UPDATE]" if cfg.config_file.exists() else "[ADD]"

        if dry_run:
            print(f"    {action} {rel}")
        else:
            ensure_dir(cfg.config_file.parent)
            atomic_write(cfg.config_file, content)

        if action == "[ADD]":
            result.added += 1
        else:
            result.updated += 1

    print_summary("Config", result)


