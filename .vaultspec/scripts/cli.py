# ---------------------------------------------------------------------------
# YAML parsing: prefer PyYAML, fall back to simple parser
# ---------------------------------------------------------------------------
try:
    import yaml

    def _yaml_load(text: str) -> dict[str, Any]:
        return yaml.safe_load(text) or {}

    def _yaml_dump(data: dict[str, Any]) -> str:
        return yaml.dump(
            data, default_flow_style=False, allow_unicode=True, sort_keys=False
        ).rstrip("\n")

except ImportError:
    yaml = None  # type: ignore

    def _yaml_load(text: str) -> dict[str, Any]:
        """Minimal 2-level YAML parser."""
        data: dict[str, Any] = {}
        current_key: str | None = None
        for line in text.splitlines():
            if not line.strip():
                continue
            # Indented line -> nested under current_key
            if line.startswith("  ") and current_key is not None:
                stripped = line.strip()
                if ":" in stripped:
                    k, v = stripped.split(":", 1)
                    if not isinstance(data[current_key], dict):
                        data[current_key] = {}
                    data[current_key][k.strip()] = v.strip()
                continue
            # Top-level key
            if ":" in line:
                k, v = line.split(":", 1)
                k = k.strip()
                v = v.strip()
                if v:
                    # Strip quotes
                    if (v.startswith('"') and v.endswith('"')) or (
                        v.startswith("'") and v.endswith("'")
                    ):
                        v = v[1:-1]
                    data[k] = v
                else:
                    data[k] = {}
                current_key = k
        return data

    def _yaml_dump(data: dict[str, Any]) -> str:
        """Minimal YAML serializer."""
        lines: list[str] = []
        for k, v in data.items():
            if isinstance(v, dict):
                lines.append(f"{k}:")
                for sk, sv in v.items():
                    lines.append(f"  {sk}: {sv}")
            elif isinstance(v, str) and (
                ":" in v or '"' in v or "'" in v or v != v.strip()
            ):
                lines.append(f'{k}: "{v}"')
            else:
                lines.append(f"{k}: {v}")
        return "\n".join(lines)


