def rules_add(args: argparse.Namespace) -> None:
    ensure_dir(RULES_CUSTOM_DIR)

    file_name = args.name if args.name.endswith(".md") else f"{args.name}.md"
    file_path = RULES_CUSTOM_DIR / file_name

    if file_path.exists() and not args.force:
        print(f"Error: Rule '{file_name}' exists. Use --force to overwrite.")
        return

    content = args.content

    if not content:
        if sys.stdin.isatty():
            editor = os.environ.get("EDITOR", "zed -w")
            scaffold = f"---\nname: {args.name}\n---\n\n# Rule content\n"
            file_path.write_text(scaffold, encoding="utf-8")
            print(f"Opening editor ({editor}) for {file_path}...")
            try:
                subprocess.call([*editor.split(), str(file_path)])
                print(f"Rule saved to {file_path}")
            except Exception as e:
                print(f"Error opening editor: {e}")
            return
        else:
            content = sys.stdin.read()

    fm = {"name": args.name}
    full = build_file(fm, (content or "").lstrip())
    file_path.write_text(full, encoding="utf-8")
    print(f"Created custom rule: {file_path}")


