def collect_system_parts() -> dict[str, tuple[Path, dict[str, Any], str]]:
    """Collect system prompt parts from .vaultspec/system/."""
    sources: dict[str, tuple[Path, dict[str, Any], str]] = {}
    if not SYSTEM_SRC_DIR.exists():
        return sources
    for f in sorted(SYSTEM_SRC_DIR.glob("*.md")):
        content = f.read_text(encoding="utf-8")
        meta, body = parse_frontmatter(content)
        sources[f.stem] = (f, meta, body)
    return sources


def _collect_agent_listing() -> str:
    """Build a markdown section listing available agents."""
    agents = collect_agents()
    if not agents:
        return ""
    lines = [
        "## Available Sub-Agents",
        "",
        "Sub-agents are specialized expert agents available in `.vaultspec/agents/`.",
        "Dispatch using the `spec-subagent` skill.",
        "",
    ]
    for name, meta_tuple in agents.items():
        _path, meta, _body = meta_tuple
        agent_name = Path(name).stem
        tier = meta.get("tier", "?")
        description = meta.get("description", "")
        lines.append(f"- **{agent_name}** ({tier}): {description}")
    return "\n".join(lines)


def _collect_skill_listing() -> str:
    """Build a markdown section listing available skills."""
    skills = collect_skills()
    if not skills:
        return ""
    lines = [
        "## Available Skills",
        "",
    ]
    for name, meta_tuple in skills.items():
        _path, meta, _body = meta_tuple
        description = meta.get("description", "")
        lines.append(f"- **{name}**: {description}")
    return "\n".join(lines)


def _generate_system_prompt(cfg: ToolConfig) -> str | None:
    """Assemble system prompt for a tool from .vaultspec/system/ parts."""
    if cfg.system_file is None:
        return None

    parts = collect_system_parts()
    if not parts:
        return None

    assembled: list[str] = [CONFIG_HEADER]

    # 1. base.md body first
    if "base" in parts:
        _path, _meta, body = parts["base"]
        assembled.append(body)

    # 2. Tool-specific parts (where meta "tool" matches cfg.name)
    for name, (_path, meta, body) in sorted(parts.items()):
        if name == "base":
            continue
        tool_filter = meta.get("tool")
        if tool_filter is not None and tool_filter == cfg.name:
            assembled.append(body)

    # 3. Auto-generated agent listing
    agent_listing = _collect_agent_listing()
    if agent_listing:
        assembled.append(agent_listing)

    # 4. Auto-generated skill listing
    skill_listing = _collect_skill_listing()
    if skill_listing:
        assembled.append(skill_listing)

    # 5. Remaining shared parts (no "tool" key, not "base") in sorted order
    for name, (_path, meta, body) in sorted(parts.items()):
        if name == "base":
            continue
        if meta.get("tool") is not None:
            continue
        assembled.append(body)

    return "\n\n".join(assembled)


def system_show(_args: argparse.Namespace) -> None:
    """Display system parts table and generation targets."""
    parts = collect_system_parts()
    if not parts:
        print("No system parts found in .vaultspec/system/")
        return

    print(f"{'Name':<25} {'Tool Filter':<15} {'Lines':<8}")
    print("-" * 48)
    for name, (_path, meta, body) in sorted(parts.items()):
        tool_filter = meta.get("tool", "-")
        line_count = len(body.strip().splitlines()) if body.strip() else 0
        print(f"{name:<25} {tool_filter:<15} {line_count:<8}")

    print("\nGeneration targets:")
    for tool_name, cfg in TOOL_CONFIGS.items():
        if cfg.system_file is None:
            continue
        rel = cfg.system_file.relative_to(ROOT_DIR)
        managed = "CLI-managed" if _is_cli_managed(cfg.system_file) else "custom"
        print(f"  {tool_name}: {rel} [{managed}]")


def system_sync(args: argparse.Namespace) -> None:
    """Sync assembled system prompts to tool destinations."""
    dry_run = getattr(args, "dry_run", False)
    force = getattr(args, "force", False)

    result = SyncResult()

    for _tool_name, cfg in TOOL_CONFIGS.items():
        if cfg.system_file is None:
            continue

        content = _generate_system_prompt(cfg)
        if content is None:
            result.skipped += 1
            continue

        rel = cfg.system_file.relative_to(ROOT_DIR)

        # Safety guard
        if (
            cfg.system_file.exists()
            and not _is_cli_managed(cfg.system_file)
            and not force
        ):
            print(f"    [SKIP] {rel} - file exists with custom content.")
            print("           Use --force to overwrite.")
            result.skipped += 1
            continue

        action = "[UPDATE]" if cfg.system_file.exists() else "[ADD]"

        if dry_run:
            print(f"    {action} {rel}")
        else:
            ensure_dir(cfg.system_file.parent)
            atomic_write(cfg.system_file, content)

        if action == "[ADD]":
            result.added += 1
        else:
            result.updated += 1

    print_summary("System", result)


def print_summary(resource: str, result: SyncResult) -> None:
    parts = []
    if result.added:
        parts.append(f"{result.added} added")
    if result.updated:
        parts.append(f"{result.updated} updated")
    if result.pruned:
        parts.append(f"{result.pruned} pruned")
    if result.skipped:
        parts.append(f"{result.skipped} skipped")
    if result.errors:
        parts.append(f"{len(result.errors)} errors")
    summary = ", ".join(parts) if parts else "no changes"
    print(f"  {resource}: {summary}")


