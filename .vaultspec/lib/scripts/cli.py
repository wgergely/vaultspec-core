try:
    import yaml

    def _yaml_load(text: str) -> dict[str, Any]:
        return yaml.safe_load(text) or {}

    class _LiteralStr(str):
        """Marker type for strings that should use YAML literal block scalar."""

    def _literal_representer(dumper: yaml.Dumper, data: _LiteralStr) -> yaml.ScalarNode:
        return dumper.represent_scalar("tag:yaml.org,2002:str", data, style="|")

    yaml.add_representer(_LiteralStr, _literal_representer)

    def _yaml_dump(data: dict[str, Any]) -> str:
        prepared = {}
        for k, v in data.items():
            if isinstance(v, str) and "\n" in v:
                prepared[k] = _LiteralStr(v)
            else:
                prepared[k] = v
        return yaml.dump(
            prepared, default_flow_style=False, allow_unicode=True, sort_keys=False
        ).rstrip("\n")

except ImportError:

    def _yaml_load(text: str) -> dict[str, Any]:
        """Minimal 2-level YAML parser."""
        data: dict[str, Any] = {}
        current_key: str | None = None
        for line in text.splitlines():
            if not line.strip():
                continue
            # Indented line -> nested under current_key
            if line.startswith("  ") and current_key is not None:
                stripped = line.strip()
                if ":" in stripped:
                    k, v = stripped.split(":", 1)
                    if not isinstance(data[current_key], dict):
                        data[current_key] = {}
                    data[current_key][k.strip()] = v.strip()
                continue
            # Top-level key
            if ":" in line:
                k, v = line.split(":", 1)
                k = k.strip()
                v = v.strip()
                if v:
                    # Strip quotes
                    if (v.startswith('"') and v.endswith('"')) or (
                        v.startswith("'") and v.endswith("'")
                    ):
                        v = v[1:-1]
                    data[k] = v
                else:
                    data[k] = {}
                current_key = k
        return data

    def _yaml_dump(data: dict[str, Any]) -> str:
        """Minimal YAML serializer."""
        lines: list[str] = []
        for k, v in data.items():
            if isinstance(v, dict):
                lines.append(f"{k}:")
                for sk, sv in v.items():
                    lines.append(f"  {sk}: {sv}")
            elif isinstance(v, bool):
                lines.append(f"{k}: {'true' if v else 'false'}")
            elif isinstance(v, str) and (
                ":" in v or '"' in v or "'" in v or v != v.strip()
            ):
                lines.append(f'{k}: "{v}"')
            else:
                lines.append(f"{k}: {v}")
        return "\n".join(lines)


