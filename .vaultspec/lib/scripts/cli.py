@dataclass
class SyncResult:
    added: int = 0
    updated: int = 0
    skipped: int = 0
    pruned: int = 0
    errors: list[str] = field(default_factory=list)


from vault.parser import parse_frontmatter  # noqa: E402


def build_file(frontmatter: dict[str, Any], body: str) -> str:
    """Assemble a file from frontmatter dict and body content."""
    fm_str = _yaml_dump(frontmatter)
    return f"---\n{fm_str}\n---\n\n{body}"


def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def atomic_write(path: Path, content: str) -> None:
    """Write content to file atomically via tmp + rename."""
    tmp = path.with_suffix(path.suffix + ".tmp")
    tmp.write_text(content, encoding="utf-8")
    tmp.replace(path)


def resolve_model(tool: str, tier: str) -> str | None:
    """Resolve a capability tier name to a model string."""
    if not PROVIDERS or tool not in PROVIDERS:
        return None
    try:
        from protocol.providers.base import CapabilityLevel

        level = CapabilityLevel[tier.upper()]
    except (ImportError, KeyError, AttributeError):
        return None
    return PROVIDERS[tool].get_best_model_for_capability(level)


def collect_rules() -> dict[str, tuple[Path, dict[str, Any], str]]:
    """Collect rules (builtin *.builtin.md + custom *.md) from rules/."""
    sources: dict[str, tuple[Path, dict[str, Any], str]] = {}

    if not RULES_SRC_DIR.exists():
        return sources
    for f in sorted(RULES_SRC_DIR.glob("*.md")):
        content = f.read_text(encoding="utf-8")
        meta, body = parse_frontmatter(content)
        sources[f.name] = (f, meta, body)

    return sources


def transform_rule(tool: str, name: str, _meta: dict[str, Any], body: str) -> str:
    """Transform a rule for a specific tool destination."""
    fm: dict[str, Any] = {}
    if tool != "agent":
        fm["name"] = Path(name).stem
    fm["trigger"] = "always_on"
    return build_file(fm, body)


